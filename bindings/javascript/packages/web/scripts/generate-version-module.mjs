import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const pkgRoot = resolve(__dirname, '..');
const packageJsonPath = resolve(pkgRoot, 'package.json');
const outPath = resolve(pkgRoot, 'src', 'version.generated.js');

const packageInfo = JSON.parse(readFileSync(packageJsonPath, 'utf8'));

if (typeof packageInfo.version !== 'string' || packageInfo.version.length === 0) {
    throw new Error(`Missing string field version in ${packageJsonPath}`);
}

if (
    typeof packageInfo.wkpCoreCompatibility !== 'string'
    || packageInfo.wkpCoreCompatibility.length === 0
) {
    throw new Error(`Missing string field wkpCoreCompatibility in ${packageJsonPath}`);
}

const content = [
    '// Generated by scripts/generate-version-module.mjs. Do not edit manually.',
    `export const BINDING_VERSION = ${JSON.stringify(packageInfo.version)};`,
    `export const CORE_COMPATIBILITY = ${JSON.stringify(packageInfo.wkpCoreCompatibility)};`,
    ''
].join('\n');

writeFileSync(outPath, content, 'utf8');
console.log(`Wrote ${outPath}`);